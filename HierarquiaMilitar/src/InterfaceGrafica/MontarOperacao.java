     private void gerarXMLProlog(PrologFile prolog) {
         int numeroSoldados=0;
         switch (operacao.getDificuldade()){
             case "Díficil": numeroSoldados = 7;
                 break;
             case "Médio": numeroSoldados=3;
                 break;
             case "Fácio": numeroSoldados=1;
                 break;
             default: numeroSoldados=-1;
         }
         
         try {
            prolog.escritorArquivo("numeroSoldados(X, Y):-proper_lenght(X,Y)"
                    + ".");

            prolog.escritorArquivo("adcionaSoldado(Xlist, Yrequisito):-append(Xlist, requisitos(Z,Yrequisito),Xlist).");
            prolog.escritorArquivo("percorreSoldados(Ysoldado,Xlist,0):-nth0(0,Xlist,Ysoldado)!.");
            prolog.escritorArquivo("percorreSoldados(Ysoldado,Xlist,Znumero):-"
                    + "percorreSoldados(Ysoldado,Xlist,Znumero-1)"
                    + "nth0(Znumero,Xlist,K)"
                    + ".");
            PrologPackage.PrologInterpreter intw = new PrologInterpreter("adcionaSoldado(X, '"+ operacao.getRequisitos().get(0) + "').");
            
             Hashtable[] hash = intw.todasSolucoes(intw.getExecutor());
            prolog.escritorArquivo("criaXML(ARQ,ZnumeroSoldados):-"
                    + "tell(ARQ)"
                    + "write('<OPERACAO id=+"+ operacao.getIdOperacao()+ ">'), nl,"
                    + "write('<SOLDADO>', percorreSoldados(Y, ["
                    + ""
                    + ""
                    + ".");
        } catch (IOException ex) {
            Logger.getLogger(MontarOperacao.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
