    private ArrayList getRequisitos()
    {
        ArrayList botoes = null;
        AbstractButton botao;
        boolean add = false;
        
        //for (Enumeration<E> e = v.elements(); e.hasMoreElements();)
        for (Enumeration e  = buttonGroup2.getElements(); e.hasMoreElements();)
        {
            botao = (AbstractButton) e.nextElement();
            if (botao.isSelected())
            {
               add  = botoes.add(botao.getText());
            }
        }
        if (add == true)
            return botoes;
        return null;
    }
    
